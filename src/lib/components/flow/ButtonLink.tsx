interface IButtonLink {
  // Href when clicking button
  href: string;

  // Text for the button
  text: string;
}

/**
 * A button to another page in the flow.
 */
const ButtonLink: React.FC<IButtonLink> = ({ href, text }: IButtonLink) => {
  const router = useRouter();
  useHotkeys("enter, right", () => {
    router.push(href);
  });

  return (
    <Flex minW="65%" direction="column">
      <Link href={href} passHref>
        <Button
          mt={4}
          minW="65%"
          bg="#85E0B4"
          size="lg"
          _hover={{ bg: "green.600" }}
          rightIcon={<PathIcon boxSize={12} />}
        >
          <Text fontSize="2xl">{text}</Text>
        </Button>
      </Link>
    </Flex>
  );
};

export default ButtonLink;
